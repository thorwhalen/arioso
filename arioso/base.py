@dataclass(frozen=True)
class Affordance:
    """Descriptor for a unified parameter affordance.

    Attributes:
        name: Canonical parameter name used at the facade level.
        type: Expected Python type.
        description: Human-readable description.
        default: Default value (None means no default / required).
    """

    name: str
    type: type
    description: str
    default: Any = None


