"""Core types and constants for arioso.

Defines the unified output types (Song, AudioResult) and the canonical
set of 40 affordance names that all platform adapters map to.
"""

from typing import Optional, Any
from dataclasses import dataclass, field


@dataclass
class AudioResult:
    """Unified audio output from any music generation platform.

    Exactly one of audio_bytes, audio_url, or audio_array will typically
    be populated by the platform adapter. Conversion between forms can
    be done on demand by the caller.

    Attributes:
        audio_bytes: Raw audio file bytes (MP3, WAV, etc.).
        audio_url: URL to download the audio file.
        audio_array: Numpy array of audio samples.
        sample_rate: Sample rate in Hz (e.g. 44100, 32000).
        format: Audio format string (e.g. "mp3", "wav", "pcm").
        duration_seconds: Duration of the audio in seconds.
    """

    audio_bytes: Optional[bytes] = field(default=None, repr=False)
    audio_url: str = ""
    audio_array: Optional[Any] = field(default=None, repr=False)
    sample_rate: int = 0
    format: str = ""
    duration_seconds: float = 0.0


@dataclass
class Song:
    """A generated song with its metadata and audio data.

    Attributes:
        id: Provider-specific identifier for the song.
        title: Song title (may be auto-generated by the platform).
        status: Generation status: pending, generating, complete, error.
        audio: The audio output.
        metadata: Provider-specific metadata dict.
        platform: Name of the platform that generated this song.
    """

    id: str = ""
    title: str = ""
    status: str = "pending"
    audio: AudioResult = field(default_factory=AudioResult)
    metadata: dict = field(default_factory=dict)
    platform: str = ""

    @property
    def is_error(self) -> bool:
        """Check if this Song represents an error response."""
        if self.status == "error":
            return True
        meta_code = self.metadata.get("code") if self.metadata else None
        if meta_code and meta_code != 200:
            return True
        return False

    @property
    def error_message(self) -> str:
        """Extract error message from metadata, if any."""
        if self.metadata:
            msg = self.metadata.get("msg", "")
            if msg:
                code = self.metadata.get("code", "")
                return f"(code {code}): {msg}" if code else msg
        if self.status == "error":
            return "Unknown error"
        return ""

    @property
    def audio_bytes(self) -> Optional[bytes]:
        """Shortcut to audio.audio_bytes."""
        return self.audio.audio_bytes

    @property
    def audio_url(self) -> str:
        """Shortcut to audio.audio_url."""
        return self.audio.audio_url

    @property
    def audio_array(self) -> Optional[Any]:
        """Shortcut to audio.audio_array."""
        return self.audio.audio_array

    @property
    def sample_rate(self) -> int:
        """Shortcut to audio.sample_rate."""
        return self.audio.sample_rate


